def upper(string):
    '''
    :param string:
    :return: UpperCase of String
    '''
    return string.upper()


def capit(text):
    '''
    :param text:
    :return: capitalize the first letter of the string
    '''
    return text.capitalize()


def capit_all_words(text):
    '''
    :param text:
    :return: capitalize each of the words in a string
    '''
    return text.title()